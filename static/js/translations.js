/* ─── Helpers ──────────────────────────────────────────────── */
function getLang() {
    const s = localStorage.getItem('lang');
    return (s && LANGS.includes(s)) ? s : DEFAULT;
}

function setLang(code) {
    if (!LANGS.includes(code)) code = DEFAULT;
    localStorage.setItem('lang', code);
    document.documentElement.lang = code;
    applyI18n();
    _updateSwitcherDisplay(code);
    // date locale
    window._i18nLocale = code === 'en' ? 'en-GB' : 'fr-FR';
}

function getNestedKey(obj, key) {
    return key.split('.').reduce(function(o, k) { return o ? o[k] : undefined; }, obj);
}

function t(key, vars) {
    const lang = getLang();
    const dict = D[lang] || D[DEFAULT];
    let str = getNestedKey(dict, key);
    if (str === undefined) str = getNestedKey(D[DEFAULT], key);
    if (str === undefined) str = key;
    if (vars && typeof str === 'string') {
        str = str.replace(/\{\{(\w+)\}\}/g, function(_, k) {
            return vars[k] !== undefined ? vars[k] : '{{' + k + '}}';
        });
    }
    return str !== undefined ? str : key;
}

function applyI18n() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        const key = el.getAttribute('data-i18n');
        const val = t(key);
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = val;
        } else {
            // preserve icon children
            const icons = Array.from(el.querySelectorAll('i, svg, span.badge'));
            el.textContent = val;
            icons.forEach(function(ic) { el.insertBefore(ic, el.firstChild); });
        }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
        el.innerHTML = t(el.getAttribute('data-i18n-html'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
        el.title = t(el.getAttribute('data-i18n-title'));
    });
}

function _updateSwitcherDisplay(code) {
    const flags = { fr: '🇫🇷', en: '🇬🇧', wo: '🇸🇳' };
    const names = { fr: 'FR', en: 'EN', wo: 'WO' };
    const label = (flags[code] || '🌐') + ' ' + (names[code] || code.toUpperCase());
    document.querySelectorAll('.lang-current-label').forEach(function(el) {
        el.textContent = label;
    });
}

function toggleLangMenu(e) {
    if (e) e.stopPropagation();
    // Trouver le .lang-menu dans le même .lang-switcher que le bouton cliqué
    var btn = e && e.currentTarget ? e.currentTarget : (e && e.target ? e.target : null);
    var switcher = btn ? btn.closest('.lang-switcher') : null;
    var menu = switcher ? switcher.querySelector('.lang-menu') : null;
    if (!menu) return;
    var open = menu.style.display !== 'none';
    // Fermer tous les menus ouverts
    document.querySelectorAll('.lang-menu').forEach(function(m) { m.style.display = 'none'; });
    if (!open) menu.style.display = 'block';
}

function closeLangMenu() {
    document.querySelectorAll('.lang-menu').forEach(function(m) { m.style.display = 'none'; });
}

function _init() {
    const code = getLang();
    document.documentElement.lang = code;
    window._i18nLocale = code === 'en' ? 'en-GB' : 'fr-FR';
    applyI18n();
    _updateSwitcherDisplay(code);

    // Fermer les menus langue en cliquant en dehors
    document.addEventListener('click', function(e) {
        var inside = e.target.closest('.lang-switcher');
        if (!inside) closeLangMenu();
    });
}

/* ─── Expose ───────────────────────────────────────────────── */
window.t           = t;
window.getLang     = getLang;
window.setLang     = setLang;
window.applyI18n   = applyI18n;
window.toggleLangMenu = toggleLangMenu;
window.closeLangMenu  = closeLangMenu;

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', _init);
} else {
    _init();
}

